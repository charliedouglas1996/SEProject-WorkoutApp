public class WorkoutStats {

	private int count;
	private int totalTime;

	public int getCount() {
		return this.count;
	}

	public int getTotalTime() {
		return this.totalTime;
	}

	public void setCount(int c) {
		this.count = c;
	}

	public void setTotalTime(int exerciseDuration) {
		this.totalTime = exerciseDuration;
	}

}